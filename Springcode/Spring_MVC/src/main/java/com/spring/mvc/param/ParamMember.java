package com.spring.mvc.param;

public class ParamMember {
	
	private String name;
	private Integer age;
	
	// 기본생성자
	public ParamMember() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}
	
	
	
}
